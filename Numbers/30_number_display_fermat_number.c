#include<stdio.h>
#include<stdlib.h>
#include<math.h>

int main()
{
    int n = 0;
    double result;

    printf("The first 10 Fermet numbers are: \n");
    while(n <= 10)
    {
        result = pow(2, pow(2, n)) + 1;
        n++;
        printf("%lf \n", result);
    }
}